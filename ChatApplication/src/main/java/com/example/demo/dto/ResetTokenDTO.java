package com.example.demo.dto;

public class ResetTokenDTO {
	private String token;
	
	public ResetTokenDTO() {
		
	}

	public ResetTokenDTO(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	
	
}
